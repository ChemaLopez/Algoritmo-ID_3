package Decision;

public class Infor {

	private int valor;
	private int p;
	private int n;
	
	
	public Infor(int valor, int p , int n){
		this.valor=valor;
		this.p=p;
		this.n=n;
		
	}
	
	
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}
	public int getP() {
		return p;
	}
	public void setP(int p) {
		this.p = p;
	}
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}
	
	
	
	
	
}
